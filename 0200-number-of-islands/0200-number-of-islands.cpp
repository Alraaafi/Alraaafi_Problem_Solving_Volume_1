class Solution {

int vis[302][302];

void dfs(int i, int  j , vector<vector<char>>& g)
{
    int n = g.size();
    int m = g[0].size();

    if(i>=n || i<0 || j<0 || j>=m || vis[i][j] || g[i][j]!='1') return ;

    vis[i][j] = 1;

    dfs(i+1,j, g);
    dfs(i-1,j, g );
    dfs(i,j+1, g);
    dfs(i,j-1, g);

}

public:
    int numIslands(vector<vector<char>>& gr) {
        int n = gr.size();
        int m = gr[0].size();
        memset(vis, 0, sizeof(vis));
        int cnt = 0;
        
        for(int i=0; i<n; i++)
        {
            for(int j=0; j<m; j++)
            {
                if(vis[i][j] || gr[i][j]!='1') continue;

                dfs(i,j,gr);
                cnt++;
            }
        }
        return cnt;
    }
};