class Solution {

void dfs(int i, int  j, int oldCol, int newCol, vector<vector<int>>& img)
{
    int n = img.size();
    int m = img[0].size();
    if(i>=n || i<0 || j<0 || j>=m) return ;
    if( img[i][j] != oldCol ) return ;

    img[i][j] = newCol;
    dfs(i+1,j, oldCol, newCol, img);
    dfs(i-1,j, oldCol, newCol, img);
    dfs(i,j+1, oldCol, newCol, img);
    dfs(i,j-1, oldCol, newCol, img);
    
}

public:
    
    vector<vector<int>> floodFill(vector<vector<int>>& img, int i, int j, int newCol) {
        if(img[i][j]!=newCol)
        dfs(i,j,img[i][j], newCol, img);
        return img;
    }
};